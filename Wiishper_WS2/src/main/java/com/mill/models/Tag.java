package com.mill.models;

public class Tag {
	
	long idtags;
	String name;
	
	public long getIdtags()
	{
		return idtags;
	}
	public void setIdtags(long idtags)
	{
		this.idtags = idtags;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}
}
