package com.mill.models;

public class Country {
	
	long idcountries;
	String name;
	
	public long getIdcountries()
	{
		return idcountries;
	}
	public void setIdcountries(long idcountries)
	{
		this.idcountries = idcountries;
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name)
	{
		this.name = name;
	}	

}
